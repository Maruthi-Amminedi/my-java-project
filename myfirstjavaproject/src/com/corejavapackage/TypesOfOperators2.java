package com.corejavapackage;

public class TypesOfOperators2 {

	public static void main(String[] args) {
		System.out.println("main method started");
		int a = 20;
		int result = 0;

		result = result + a;
		result = result + 10;
		result += result + 4.5;
		result -= result - 4.5;
		System.out.println("Result = " + result);

	}

}
